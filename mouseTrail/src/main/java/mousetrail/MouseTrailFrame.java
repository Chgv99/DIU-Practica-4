    private void trailColorBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_trailColorBoxActionPerformed
        lienzo.setMouseTrailColor(getTrailColorBoxColor());
        previewColor.setBackground(getTrailColorBoxColor());
        boolean customColor = trailColorBox.getSelectedIndex() == 3;

        redSlider.setEnabled(customColor);
        greenSlider.setEnabled(customColor);
        blueSlider.setEnabled(customColor);
    }//GEN-LAST:event_trailColorBoxActionPerformed

    private Color getTrailColorBoxColor() {
        switch (trailColorBox.getSelectedIndex()) {
            case 0: //BLACK
                return Color.BLACK;
            case 1: //RED
                return Color.RED;
            case 2: //GREEN
                return Color.GREEN;
            case 3:
                return getCustomColor();
            default:
                throw new AssertionError();
        }
    }

    private Color getCustomColor() {
        return new Color(redSlider.getValue(), greenSlider.getValue(), blueSlider.getValue());
    }

