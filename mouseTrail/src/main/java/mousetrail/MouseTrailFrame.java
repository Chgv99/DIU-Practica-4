    private Color getBackgroundColorBoxColor() {
        switch (backgroundColorBox.getSelectedIndex()) {
            case 0: //WHITE
                return Color.WHITE;
            case 1: //GRAY
                return Color.GRAY;
            case 2: //YELLOW
                return Color.YELLOW;
            default:
                throw new AssertionError();
        }
    }

